public class Address {
    public   String street;
    public   int streetNo;
    public   String section;
    public   String city;

    public Address() {
    }

    public String getStreet() {
        return street;
    }

    public int getStreetNo() {
        return streetNo;
    }

    public String getSection() {
        return section;
    }

    public String getCity() {
        return city;
    }
}
